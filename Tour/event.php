       <h2>Background</h2> 
      
       <p>The Hamar, also spelled Hamer, are an ethnic group inhabiting southwestern Ethiopia, particularly the Omo Valley.</p>
       <p>They are known for their unique culture, traditions, and striking appearance.</p>
      
       <h3>Culture and Lifestyle</h3>

       <p> The Hamar are primarily pastoralists, relying on cattle herding as a central part of their economy and way of life.</p>
       <p> They live in traditional homesteads made of wood, grass, and mud, often clustered together in settlements called villages .</p>
       <p> Social organization is based on age sets, with each stage of life marked by specific rituals and responsibilities.</p>
       <p> The Hamar practice a form of ritualized body decoration, including hairstyling with clay, beads, and feathers, and</p>
       <p> scarification, which is believed to enhance beauty and indicate social status.</p>

       <h3>climate condition of hamar</h3>
       <p>The Hamar people live in the Omo Valley in southwestern Ethiopia, an area characterized by a semi-arid climate. </p>
       <p> The climate in the region is typically hot and dry, with temperatures often exceeding 30°C (86°F) during the day. </p>
       <p> The region experiences two main seasons:</p>
